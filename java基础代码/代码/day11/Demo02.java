/*输出一个小数的形状*/
public class Demo02{
	public static void main(String []args){
		for(int i=1;i<=9;i++){
			for(int j=i;j<=9;j++){
				System.out.print("- ");
			for(int i=1;i<=9;i++){
				for(int j=1;j<=9;j++){
				System.out.print("* ");
			}
			System.out.println();
		}	
			}
			System.out.println();
		   }
		
	}
}