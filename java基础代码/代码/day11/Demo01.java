/*二维数组*/
public class Demo01{
	public static void main(String []args){
		int[][] a={{1,2,6},{9,5,6,7},{8,7,5}};
		System.out.println(a[0][1]);
		System.out.println(a[1][2]);
		System.out.println(a[2][1]);
		System.out.println("二维数组的长度是:"+a.length);
		System.out.println("二维数组第一行中元素个数为"+a[0].length);
	}
}